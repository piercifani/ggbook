- (void)configureCell:(UITableViewCell *)cell atIndexPath:(NSIndexPath *)indexPath
{
    Book *aBook = [self.fetchedResultsController objectAtIndexPath:indexPath];
    cell.textLabel.text = aBook.title;
}

@end
